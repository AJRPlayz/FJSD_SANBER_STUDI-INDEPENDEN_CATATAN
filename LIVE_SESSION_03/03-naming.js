// ===== Penamaan variable harus yang deskriptif dan bermakna =====
// contoh deskriptif dan bermakna
let umur = 10;
// contoh tidak deskriptif dan tidak bermakna
let x = 10;

// ===== Penggunaan huruf besar dan kecil =====
// 01. camel case (huruf pertama kata pertama kecil, namun huruf pertama kata2 berikutnya kapital)
let umurArjunaRibal = 20;
// 02. pascal case (huruf pertama semua kata kapital)
let UmurEdgarSilvanus = 21;
// 03. snake case (setiap kata disambung oleh _ )
let umur_michiko_tjiang = 19;
// 04. uppercase
let UMUR_ANDREAN = 18;

// ===== Konsistensi =====
// contoh good konsistensi:
let totalBuku = 10;
let totalPulpen = 15;
// contoh bad konsistensi:
let total_belanjaan = 100000;
let totalKembalian = 30000;

// ===== Hindari kata kunci terbatas =====
// ==> Jangan menggunakan keyword2 yang sudah dipakai JavaScript
// contoh:
let const = 10;
const function = "Action";

// ===== Singkatan Bermakna =====
const maximalCharacter = 20;
// disingkat menjadi:
const maxChar = 20;
